    public void displayGameState(GameState gs){
        displayBoard(gs.GetBoard());
        displayKataTerbentuk(gs.getKataTerbentuk());
        displayTotalScore(gs.totalScore());
    }
    
    public void displayTotalScore(int totalScore){
        jLabel18.setText("Total Score: " + totalScore);
    }
    
    public void displayKataTerbentuk(SortedSet<Kata> kataTerbentuk){
        NavigableSet<Kata> kataTerbentukTmp = new TreeSet<Kata>(kataTerbentuk);
        String displayText = "";
        for (Iterator<Kata> it = kataTerbentukTmp.descendingIterator();it.hasNext();){
            Kata elem = it.next();
            displayText += elem.getScore() + "\t" + elem.getK() + "\n";
        }
        jTextArea1.setText(displayText);
    }
    
    public void displayBoard(Board b){
        for (int i=0;i<4;i++){
            for (int j=0;j<4;j++){
                String s = "";
                if (b.getVisited()[i][j])
                    s += "*";
                s+=b.getBoard()[i][j];
                labels[i][j].setText(s);
            }
        }
        JLabel pointedLabel = labels[b.getPointer().x][b.getPointer().y];
        pointedLabel.setText(pointedLabel.getText()+ "<");
        jLabel17.setText(b.yangSedangDibentuk());
    }
    
